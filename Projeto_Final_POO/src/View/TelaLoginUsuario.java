    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().trim().equals("") || jPasswordField1.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.", "Alerta", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                try{
                    File arquivo = new File("Usu√°rios.txt");
                    Scanner read = new Scanner(arquivo);
                    while(read.hasNextLine()){
//                        Enquanto existir uma pr√≥xima linha
                        String dados = read.nextLine();
                        
                        String[] vetor = dados.split(";");
                        // Crio vetores a partir do Split
                        
                        for(int i = 1; i < dados.length(); i += 5){
                            //Percorro os dados do meu arquivo
                            if(vetor[i].equals("MATRICULA") && jTextField1.getText().trim().equals(vetor[(i+1)]) && jPasswordField1.getText().trim().equals(vetor[(i+2)])){
                                JOptionPane.showMessageDialog(null, "Bem vindo ao Sistema, "+vetor[(i-1)]+".", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                                this.dispose();
                                TelaUsuarioVerContatos tela = new TelaUsuarioVerContatos(null, rootPaneCheckingEnabled);
                                tela.setVisible(true);
                                break;
                            }
                        }
                    }
                    read.close();
                }catch(FileNotFoundException ex){
                    // Caso o arquivo n√£o seja criado, ou n√£o encontrado.
                    JOptionPane.showMessageDialog(null, "Este arquivo n√£o existe.", "Alerta", JOptionPane.ERROR_MESSAGE);
                } catch (IOException ex) {
                    Logger.getLogger(TelaLoginUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }
            }catch(ArrayIndexOutOfBoundsException ex){
                // Caso o arquivo n√£o possua nenhum dado.
                JOptionPane.showMessageDialog(null, "Dados incorretos ou conta n√£o existente!", "Alerta", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

