    public String cadastro(){
        Contato contato = new Contato();
        
        contato.setNome(jNome.getText());
        contato.setEndereco(jEndereco.getText());
        contato.setDataDeNascimento(jData.getText());
        contato.setTelefoneFixo(jTelefone.getText());
        contato.setCelular(jCelular.getText());
        contato.setTurma(jCidade.getText());
       
        return contato.getNome()+";"
                +contato.getEndereco()+";"
                +contato.getTurma()+";"
                +contato.getDataDeNascimento()+";"
                +contato.getTelefoneFixo()+";"
                +contato.getCelular()+"\n";
    }
    
    public void lerArquivo() throws IOException{
            try{
                File arquivo = new File("Contatos.txt");
                FileReader read = new FileReader(arquivo);
                BufferedReader bufer = new BufferedReader(read);
                String dados = "";
                String txt = "";
                while((txt = bufer.readLine()) != null){
                    dados += txt+"\n";
                }
                    String[] vetor = dados.split("\n"); //Divide pelo sinal e cria vetor.                       
                        
                    for(int i = 0; i < vetor.length; i++){
                        String[] array = vetor[i].split(";");
                        DefaultTableModel dtm = (DefaultTableModel) jTabela.getModel();
                        dtm.addRow(array);
                       
                    }
//                    JOptionPane.showMessageDialog(null, "NÃO Entra");
//                    break;
            }catch(FileNotFoundException ex){
                // Caso o arquivo não seja criado, ou não encontrado.
//                JOptionPane.showMessageDialog(null, "Este arquivo não existe.", "Alerta", JOptionPane.ERROR_MESSAGE);
            }
        
    }
    
