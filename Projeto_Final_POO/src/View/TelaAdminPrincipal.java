    public void remover() throws IOException{
        String caminho = "Contatos.txt";
        File file = new File(caminho);
        FileWriter fw = new FileWriter(file.getPath());
        BufferedWriter bufer = new BufferedWriter(fw);
        
        for(int i = 0; i < jTabela.getRowCount(); i++){
            for(int k = 0; k < jTabela.getColumnCount(); k++){
                bufer.write(jTabela.getValueAt(i, k).toString()+";");
            }
            bufer.write("\n");
        }
        bufer.close();
        fw.close();
    }
    
